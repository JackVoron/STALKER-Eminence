	function createButtons()
		for i = 1, 10 do
			local x, y, z
			if i < 6 then
				x = -2.3825 + 1.19 * (i - 1)
				y = 0.15
				z = -0.6
			else
				x = -2.3825 + 1.19 * (i - 6)
				y = 0.15
				z = 0.6
			end
			local funcname = "generateName" .. i
			local func = function() printToAll(generateName({i})) end
			self.setVar(funcname, func)
			self.createButton({
				click_function = funcname,
				function_owner = self,
				position = {x, y, z},
				width = 560,
				height = 560,
				color = {0, 0, 0, 0}
			})
		end
	end
	
	function generateName(i)
		i = i[1]
		return (names[i].first_name[math.random(#names[i].first_name)] .. " " .. names[i].second_name[math.random(#names[i].second_name)])
	end
	